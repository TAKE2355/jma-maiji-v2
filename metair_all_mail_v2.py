def build_one_page(page_cfg, slot_images, page_num, total_pages, dpi):
    """1脙聝脗拢脙聜脗聝脙聜脗職脙聝脗拢脙聜脗聝脙聜脗录脙聝脗拢脙聜脗聜脙聜脗赂脙聝脗楼脙聜脗聢脙聜脗聠脙聝脗拢脙聜脗聛脙聜脗庐PIL Image脙聝脗拢脙聜脗聜脙聜脗聮脙聝脗搂脙聜脗聰脙聜脗聼脙聝脗娄脙聜脗聢脙聜脗聬脙聝脗拢脙聜脗聛脙聜脗聶脙聝脗拢脙聜脗聜脙聜脗聥脙聝脗拢脙聜脗聙脙聜脗聜"""
